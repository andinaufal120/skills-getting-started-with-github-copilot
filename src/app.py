# Function to populate the database with initial data if it's empty
def populate_initial_data():
    if activities_collection.count_documents({}) == 0:
        for name, details in initial_activities.items():
            # Use the activity name as the _id
            activity_doc = {"_id": name, **details}
            activities_collection.insert_one(activity_doc)

# Populate the database at startup
populate_initial_data()

# Mount the static files directory
current_dir = Path(__file__).parent
app.mount("/static", StaticFiles(directory=os.path.join(Path(__file__).parent,
          "static")), name="static")


@app.get("/")
def root():
    return RedirectResponse(url="/static/index.html")


@app.get("/activities")
def get_activities():
    activities = {}
    for activity in activities_collection.find():
        # Convert ObjectId to string for the key
        activity_name = activity["_id"]
        # Remove _id from the details
        del activity["_id"]
        activities[activity_name] = activity
    return activities


@app.post("/activities/{activity_name}/signup")
def signup_for_activity(activity_name: str, email: str):
    """Sign up a student for an activity"""
    # Validate activity exists
    activity = activities_collection.find_one({"_id": activity_name})
    if not activity:
        raise HTTPException(status_code=404, detail="Activity not found")

    # Add student
    # Validate student is not already signed up
    if email in activity.get("participants", []):
        raise HTTPException(status_code=409, detail="Student already signed up")

    activities_collection.update_one(
        {"_id": activity_name},
        {"$push": {"participants": email}}
    )
    return {"message": f"Signed up {email} for {activity_name}"}


@app.delete("/activities/{activity_name}/participants/{email}")
def unregister_participant(activity_name: str, email: str):
    """Unregister a participant from an activity"""
    activity = activities_collection.find_one({"_id": activity_name})
    if not activity:
        raise HTTPException(status_code=404, detail="Activity not found")

    if email not in activity.get("participants", []):
        raise HTTPException(status_code=404, detail="Participant not found")

    activities_collection.update_one(
        {"_id": activity_name},
        {"$pull": {"participants": email}}
    )
    return {"message": f"Unregistered {email} from {activity_name}"}
